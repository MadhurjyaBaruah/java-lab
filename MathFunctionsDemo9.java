public class MathFunctionsDemo9 {
    public static void main(String[] args) {
        System.out.println("Absolute of -5: " + Math.abs(-5));
        System.out.println("Max of 10 and 20: " + Math.max(10, 20));
        System.out.println("Square root of 25: " + Math.sqrt(25));
        System.out.println("Power of 2^3: " + Math.pow(2, 3));
        System.out.println("Log of 10: " + Math.log(10));
        System.out.println("Round of 3.6: " + Math.round(3.6));
        System.out.println("Ceiling of 3.4: " + Math.ceil(3.4));
        System.out.println("Floor of 3.9: " + Math.floor(3.9));
        System.out.println("Sin of 45 degrees: " + Math.sin(Math.toRadians(45)));
    }
}
